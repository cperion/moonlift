    title [str],
    kind [str],
    diagnostics [many [LalinLsp.DiagnosticPayload]],
    edits [many [LalinLsp.WorkspaceEditPayload]],
  },
  product. FoldingRangePayload {
    start_line [number],
    start_character [number],
    end_line [number],
    end_character [number],
    kind [str],
  },
  product. SelectionRangePayload {
    range [LalinLsp.ProtocolRange],
    parents [many [LalinLsp.SelectionRangePayload]],
