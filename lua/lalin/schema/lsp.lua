    message [str],
    range [optional [LalinLsp.ProtocolRange]],
  },
  product. DiagnosticPayload {
    range [LalinLsp.ProtocolRange],
    severity [number],
    code [str],
    message [str],
    context [many [LalinLsp.DiagnosticContextPayload]],
