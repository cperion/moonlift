  product. WorkspaceSymbolPayload {
    field. name [str],
    detail [str],
    kind [number],
    location [LalinLsp.Location],
    container_name [str],
  },
  product. SignatureParameterPayload { label [str], documentation [str], },
  product. SignatureInformationPayload {
    label [str],
    documentation [str],
    params [many [LalinLsp.SignatureParameterPayload]],
  },
  product. SignatureHelpPayload {
    signatures [many [LalinLsp.SignatureInformationPayload]],
    active_signature [number],
    active_parameter [number],
  },
  product. SemanticTokens { data [many [number]], },
  product. DocumentHighlightPayload { range [LalinLsp.ProtocolRange], kind [number], },
  product. PrepareRenamePayload { range [LalinLsp.ProtocolRange], placeholder [str], },
  product. TextEditPayload { range [LalinLsp.ProtocolRange], new_text [str], },
  product. WorkspaceEditPayload {
    uri [LalinSource.DocUri],
    edits [many [LalinLsp.TextEditPayload]],
