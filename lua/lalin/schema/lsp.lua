    PayloadInitialize { result [LalinLsp.InitializeResult], },
    PayloadDiagnostics { report [LalinLsp.DiagnosticReport], },
    PayloadDiagnosticDocumentReport { report [LalinLsp.DiagnosticDocumentReport], },
