    PayloadCompletion { completion [LalinLsp.CompletionList], },
    PayloadDocumentSymbols { symbols [many [LalinLsp.DocumentSymbolPayload]], },
    PayloadWorkspaceSymbols { symbols [many [LalinLsp.WorkspaceSymbolPayload]], },
    PayloadSignatureHelp { help [LalinLsp.SignatureHelpPayload], },
    PayloadLocations { locations [many [LalinLsp.Location]], },
    PayloadDocumentHighlights { highlights [many [LalinLsp.DocumentHighlightPayload]], },
    PayloadPrepareRename { result [LalinLsp.PrepareRenamePayload], },
    PayloadSemanticTokens { tokens [LalinLsp.SemanticTokens], },
    PayloadWorkspaceEdit { edits [many [LalinLsp.WorkspaceEditPayload]], },
    PayloadCodeActions { actions [many [LalinLsp.CodeActionPayload]], },
    PayloadFoldingRanges { ranges [many [LalinLsp.FoldingRangePayload]], },
    PayloadSelectionRanges { ranges [many [LalinLsp.SelectionRangePayload]], },
    PayloadInlayHints { hints [many [LalinLsp.InlayHintPayload]], },
    PayloadError { code [str], message [str], },
  },
}
