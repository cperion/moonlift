local S = require("moonlift.schema.dsl")
S.use()

return schema. MoonBind {
  sum. BindingClass {
    BindingClassLocalValue,
    BindingClassLocalCell,
    BindingClassArg { variant_unique, index [number], },
    BindingClassBlockParam { variant_unique, region_id [str], block_name [str], index [number], },
    BindingClassEntryBlockParam {
      variant_unique,
      region_id [str],
      block_name [str],
      index [number],
    },
    BindingClassContParam { variant_unique, region_id [str], cont_name [str], index [number], },
    BindingClassGlobalFunc { variant_unique, module_name [str], item_name [str], },
    BindingClassGlobalConst { variant_unique, module_name [str], item_name [str], },
    BindingClassGlobalStatic { variant_unique, module_name [str], item_name [str], },
    BindingClassExtern { variant_unique, symbol [str], },
    BindingClassOpenParam { variant_unique, param [MoonOpen.OpenParam], },
    BindingClassImport { variant_unique, import [MoonOpen.ValueImport], },
    BindingClassOpenSym { variant_unique, sym [MoonCore.OpenSym], },
    BindingClassOpenSlot { variant_unique, slot [MoonOpen.Slot], },
  },
  product. Binding {
    interned,
    field. id [MoonCore.Id],
    field. name [str],
    field. ty [MoonType.Type],
    class [MoonBind.BindingClass],
  },
  sum. Residence { ResidenceUnknown, ResidenceValue, ResidenceStack, ResidenceCell, },
  sum. ResidenceReason {
    ResidenceBecauseDefault,
    ResidenceBecauseAddressTaken,
    ResidenceBecauseMutableCell,
    ResidenceBecauseNonScalarAbi,
    ResidenceBecauseMaterializedTemporary,
    ResidenceBecauseBackendRequired,
  },
  sum. ResidenceFact {
    ResidenceFactBinding { variant_unique, binding [MoonBind.Binding], },
    ResidenceFactAddressTaken { variant_unique, binding [MoonBind.Binding], },
    ResidenceFactMutableCell { variant_unique, binding [MoonBind.Binding], },
    ResidenceFactNonScalarAbi { variant_unique, binding [MoonBind.Binding], },
    ResidenceFactMaterializedTemporary { variant_unique, binding [MoonBind.Binding], },
    ResidenceFactBackendRequired { variant_unique, binding [MoonBind.Binding], },
  },
  product. ResidenceFactSet { interned, facts [many [MoonBind.ResidenceFact]], },
  product. ResidenceDecision {
    interned,
    binding [MoonBind.Binding],
    residence [MoonBind.Residence],
    reason [MoonBind.ResidenceReason],
  },
  product. ResidencePlan { interned, decisions [many [MoonBind.ResidenceDecision]], },
  product. MachineBinding {
    interned,
    binding [MoonBind.Binding],
    residence [MoonBind.Residence],
  },
  product. MachineBindingSet { interned, bindings [many [MoonBind.MachineBinding]], },
  sum. ValueRef {
    ValueRefName { variant_unique, field. name [str], },
    ValueRefPath { variant_unique, path [MoonCore.Path], },
    ValueRefBinding { variant_unique, binding [MoonBind.Binding], },
    ValueRefHole { variant_unique, slot [MoonOpen.Slot], },
  },
  product. ValueEntry { interned, field. name [str], binding [MoonBind.Binding], },
  product. TypeEntry { interned, field. name [str], field. ty [MoonType.Type], },
  product. Env {
    interned,
    module_name [str],
    values [many [MoonBind.ValueEntry]],
    types [many [MoonBind.TypeEntry]],
    layouts [many [MoonSem.TypeLayout]],
  },
  product. ConstEntry {
    interned,
    module_name [str],
    item_name [str],
    field. ty [MoonType.Type],
    field. value [MoonTree.Expr],
  },
  product. ConstEnv { interned, entries [many [MoonBind.ConstEntry]], },
  sum. StmtEnvEffect {
    StmtEnvNoBinding,
    StmtEnvAddBinding { variant_unique, entry [MoonBind.ValueEntry], },
    StmtEnvAddBindings { variant_unique, entries [many [MoonBind.ValueEntry]], },
  },
}
