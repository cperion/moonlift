-- MOM Tags Generator
-- Collects schema union tags and generates mom_tags.lua
--
-- Strategy: Load back_tags which already knows how to load schemas safely,
-- then augment with additional tags and export to a Lua constants file.

local M = {}

function M.collect()
    -- Load back_tags which has already solved schema dependency ordering
    local BT = require("moonlift.mom.back.back_tags")

    local T = {}

    -- Copy all tags from back_tags
    for k, v in pairs(BT) do
        T[k] = v
    end

    -- Explicit non-schema tags. Keep these documented beside their native use.
    T.MC_IDENTITY = 1
    T.MC_BITCAST = 2
    T.MC_IREDUCE = 3
    T.MC_SEXTEND = 4
    T.MC_UEXTEND = 5
    T.MC_FPROMOTE = 6
    T.MC_FDEMOTE = 7
    T.MC_STOF = 8
    T.MC_UTOF = 9
    T.MC_FTOS = 10
    T.MC_FTOU = 11

    T.MB_BIN_INVALID = 0
    T.MB_BIN_INT = 1
    T.MB_BIN_FLOAT = 2
    T.MB_BIN_BIT = 3
    T.MB_BIN_SHIFT = 4

    T.SC_SURFACE_CAST = 1
    T.SC_TRUNC = 2
    T.SC_ZEXT = 3
    T.SC_SEXT = 4
    T.SC_BITCAST = 5
    T.SC_SAT_CAST = 6

    return T
end

function M.write(path)
    local T = M.collect()
    local keys = {}
    for k in pairs(T) do keys[#keys + 1] = k end
    table.sort(keys)

    local f = assert(io.open(path, "wb"))
    f:write("-- Generated by lua/moonlift/mom/build/tags_gen.lua\n")
    f:write("local T = {}\n")
    for _, k in ipairs(keys) do
        f:write(string.format("T.%s = %d\n", k, T[k]))
    end
    f:write("return T\n")
    f:close()
end

return M
