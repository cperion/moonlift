-- foundry_train.lua
-- Hypothetical recursive absorber foundry trainer for the shadow simulator.
-- This is an AOT economics model: selected proposals become abstract atoms for
-- the next layer. It does not claim native code exists yet.

local Loader = require("tools.sponjit_shadow.loader")
local Sim = require("tools.sponjit_shadow.simulate")
local Report = require("tools.sponjit_shadow.report")
local Propose = require("tools.sponjit_shadow.propose")
local Foundry = require("tools.sponjit_shadow.foundry")
local FoundryEnum = require("tools.sponjit_shadow.foundry_enumerate")
local Catalog = require("tools.sponjit_shadow.catalog")

local M = {}

local function copy_config(config)
    local out = Catalog.defaults(config)
    for k, v in pairs(config or {}) do out[k] = v end
    return out
end

local function index_absorbers(absorbers)
    local by = {}
    for _, a in ipairs(absorbers or {}) do
        local first = a.pattern and a.pattern[1]
        if first then
            by[first] = by[first] or {}
            by[first][#by[first] + 1] = a
        end
    end
    return by
end

local function contains(xs, v)
    for _, x in ipairs(xs or {}) do if x == v then return true end end
    return false
end

local function observed_for_ops(ops, fact_mode)
    local observed = {}
    for i, op in ipairs(ops or {}) do
        observed[i] = Loader.infer_observed(op, ops[i - 1], ops[i + 1], fact_mode or "balanced")
    end
    return observed
end

local function produced_for(proposal)
    local ops = proposal.ops or {}
    local nf = proposal.semantic_normal_form or ops
    local last = nf[#nf] or ops[#ops]
    local out = {}
    if last and tostring(last):find("RETURN") then out[#out + 1] = "terminal_return" end
    if last and (tostring(last):find("ADDI") or tostring(last):find("ADD") or tostring(last):find("FIELD_ADDI_UPDATE") or tostring(last):find("ARRAY_ADD_UPDATE")) then
        out[#out + 1] = "last_i64"; out[#out + 1] = "fresh_result"; out[#out + 1] = "residency:last_gpr"
    end
    if last and tostring(last):find("CALL") then out[#out + 1] = "call_returned" end
    if last and (tostring(last):find("GETFIELD") or tostring(last):find("GETTABLE") or tostring(last):find("SELF")) then out[#out + 1] = "last_value" end
    return out
end

local function class_cost(op)
    if op == "CALL" or op == "TAILCALL" then return 26 end
    if op == "GETFIELD" or op == "GETTABLE" or op == "GETTABUP" or op == "SELF" or op == "SETFIELD" or op == "SETTABLE" then return 13 end
    if op == "RETURN" or op == "RETURN0" or op == "RETURN1" then return 8 end
    if op == "JMP" or op == "TEST" or op == "FORLOOP" then return 7 end
    return 6
end

function M.absorber_from_proposal(proposal, layer, config)
    config = config or {}
    local ops = proposal.ops or {}
    local sum = 0
    for _, op in ipairs(ops) do sum = sum + class_cost(op) end
    local discount = tonumber(config.foundry_discount or 0.70) or 0.70
    if #(proposal.producers or {}) > 1 then discount = tonumber(config.foundry_ssa_discount or 0.55) or 0.55 end
    local cost = math.max(3, math.floor(sum * discount + 0.5))
    return {
        id = string.format("F%d_%s", tonumber(layer or 1), proposal.name or table.concat(ops, "_")),
        pattern = ops,
        source_ops = ops,  -- for recursive enumeration: expand atom back to source ops
        active_ops_list = proposal.active_ops,  -- SSA internal ops post-optimization
        cost = cost,
        code_size = 14 + 14 * #ops,
        observed = observed_for_ops(ops, config.fact_mode),
        checked = proposal.fact_axes or {},
        produced = produced_for(proposal),
        killed = { "last_value", "residency:last_gpr" },
        foundry_layer = layer,
        source_proposal = proposal,
        semantic_normal_form = proposal.semantic_normal_form,
        semantic_normal_form_hash = proposal.semantic_normal_form_hash,
    }
end

local function simulate_all(workloads, config)
    local results = {}
    for _, w in ipairs(workloads or {}) do results[#results + 1] = Sim.simulate(w, config) end
    return results
end

local function proposal_from_form(form, rank)
    local nf = form.normal_form or form.ops or {}
    return {
        rank = rank,
        source = "ssa_enumeration",
        key = table.concat(form.ops or {}, "|"),
        name = table.concat(nf, "_") .. "_ssa_atom",
        ops = form.ops or {},
        arity = #(form.ops or {}),
        class = "ssa_form",
        source_kind = "ssa_fact_enumerated",
        pressure = form.score,
        local_total = form.count,
        seam = 0,
        count = form.count,
        fact_axes = form.facts or form.checked_facts or {},
        implementation_hint = "generated by Foundry SSA enumeration; lower to stencil vocabulary",
        examples = form.examples,
        producers = {{
            kind = "ssa_normalized",
            normal_form = nf,
            normal_form_hash = form.hash,
            checked_facts = form.checked_facts,
            deps = form.deps,
            projection = form.projection,
            stats = form.stats,
            active_ops = form.active_ops,
        }},
        semantic_normal_form = nf,
        semantic_normal_form_hash = form.hash,
        active_ops = form.active_ops,  -- for recursive atom expansion
    }
end

local function proposals_from_ssa_enumeration(workloads, config, cap, extra_atoms)
    local e = FoundryEnum.enumerate(workloads, {
        max_arity = config.max_arity or 4,
        max_windows = config.max_windows or 80,
        max_fact_axes = config.max_fact_axes or 12,
        max_fact_combos = config.max_fact_combos or 4096,
    }, extra_atoms)
    local cands = {}
    for i, f in ipairs(e.forms or {}) do
        if i > cap then break end
        cands[#cands + 1] = proposal_from_form(f, i)
    end
    return { candidates = cands, generated_at = e.generated_at, enumeration = e.stats }
end

function M.train(workloads, config)
    config = copy_config(config)
    local layers = tonumber(config.layers or 3) or 3
    local cap = tonumber(config.layer_cap or config.max_selected or 16) or 16
    local extra = {}
    local layer_reports = {}

    for layer = 0, layers do
        local cfg = copy_config(config)
        cfg.extra_absorbers = extra
        cfg.extra_absorbers_by_first = index_absorbers(extra)
        local results = simulate_all(workloads, cfg)
        local metrics = Report.suite_metrics(results, cfg)
        local misses = Report.miss_report(results, cfg)
        local proposals
        if config.enumerate_ssa ~= nil and not config.enumerate_ssa then
            proposals = Propose.propose(misses, { max_candidates = cap, max_arity = config.max_arity or 4 })
        else
            proposals = proposals_from_ssa_enumeration(workloads, config, cap, extra)
        end
        layer_reports[#layer_reports + 1] = {
            layer = layer,
            atom_count = #extra,
            metrics = metrics,
            proposals = proposals,
        }
        if layer == layers then break end
        for i, p in ipairs(proposals.candidates or {}) do
            if i > cap then break end
            extra[#extra + 1] = M.absorber_from_proposal(p, layer + 1, config)
        end
    end

    return { layers = layer_reports, final_extra_absorbers = extra }
end

return M
