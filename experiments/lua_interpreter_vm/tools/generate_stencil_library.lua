-- Offline stencil library generator/pruner.
--
-- Input:  candidate_manifest.json from mine_stencils.lua
-- Output: promotion plan with primitive stencils and pruned closure candidates.
--
-- This is not a runtime JIT path. It is the build-time/promotion-time closure
-- engine: generate bounded-arity compounds, prune, and emit explicit products
-- for review/AOT fixture generation.
--
-- Usage:
--   luajit experiments/lua_interpreter_vm/tools/generate_stencil_library.lua \
--     [manifest.json] [out_dir] [max_depth] [max_arity]

local Contracts = require("experiments.lua_interpreter_vm.src.jit.miner_contracts")
local Builder = require("experiments.lua_interpreter_vm.src.jit.library_builder")
local fixture_builder = require("experiments.lua_interpreter_vm.src.jit.fixture_builder")

local manifest_path = arg[1] or "experiments/lua_interpreter_vm/build/stencil_mining/candidate_manifest.json"
local out_dir = arg[2] or "experiments/lua_interpreter_vm/build/stencil_library"
local max_depth = tonumber(arg[3]) or 1
local max_arity = tonumber(arg[4]) or 4

local function shell_quote(s)
    return "'" .. tostring(s):gsub("'", "'\\''") .. "'"
end

local function write_all(path, s)
    local f = assert(io.open(path, "wb"))
    f:write(s)
    f:close()
end

local function mkdir_p(path)
    local ok = os.execute("mkdir -p " .. shell_quote(path))
    if ok ~= true and ok ~= 0 then error("mkdir failed: " .. tostring(path)) end
end

local function summarize_round(round)
    local lines = {}
    lines[#lines + 1] = string.format("## Closure round %d", round.depth)
    lines[#lines + 1] = ""
    lines[#lines + 1] = string.format("input atoms: %d", round.input_count)
    lines[#lines + 1] = string.format("generated candidates: %d", round.candidate_count)
    lines[#lines + 1] = string.format("promoted candidates: %d", round.promoted_count)
    lines[#lines + 1] = ""
    lines[#lines + 1] = "| candidate | arity | ops | depth | size | expanded | benefit | status |"
    lines[#lines + 1] = "|---|---:|---:|---:|---:|---:|---:|---|"
    for i, c in ipairs(round.promoted or {}) do
        if i > 40 then lines[#lines + 1] = "| ... | | | | | | | |"; break end
        local s = c.summary or {}
        lines[#lines + 1] = string.format("| `%s` | %d | %d | %d | %d | %d | %d | %s |",
            c.name or "candidate", c.arity or 0, s.covered_ops or 0, s.closure_depth or 0,
            s.code_size or 0, s.expanded_code_size or 0, c.estimated_benefit or 0, c.status or "")
    end
    lines[#lines + 1] = ""
    return table.concat(lines, "\n")
end

local function write_report(path, plan)
    local lines = {}
    lines[#lines + 1] = "# Generated Stencil Promotion Plan"
    lines[#lines + 1] = ""
    lines[#lines + 1] = "This file is generated by `tools/generate_stencil_library.lua`."
    lines[#lines + 1] = ""
    lines[#lines + 1] = "It is a promotion plan, not proof that every candidate has physical bytes."
    lines[#lines + 1] = "Primitive entries with extracted bytes can be shipped as physical stencils; compound candidates need AOT fixture generation/promotion before runtime use."
    lines[#lines + 1] = ""
    lines[#lines + 1] = string.format("- primitives: %d", plan.primitive_count or 0)
    lines[#lines + 1] = string.format("- total library atoms after closure: %d", plan.library_count or 0)
    lines[#lines + 1] = string.format("- max arity: %d", plan.policy.max_arity)
    lines[#lines + 1] = string.format("- max depth: %d", plan.policy.max_depth)
    lines[#lines + 1] = ""
    lines[#lines + 1] = "## Primitive physical stencils"
    lines[#lines + 1] = ""
    lines[#lines + 1] = "| stencil | class | ops | size | holes | relocs |"
    lines[#lines + 1] = "|---|---|---:|---:|---:|---:|"
    for _, p in ipairs(plan.primitives or {}) do
        local s = p.summary or {}
        lines[#lines + 1] = string.format("| `%s` | %s | %d | %d | %d | %d |",
            p.name, p.class or "", s.covered_ops or 0, s.code_size or 0, s.hole_count or 0, s.reloc_count or 0)
    end
    lines[#lines + 1] = ""
    for _, round in ipairs(plan.rounds or {}) do lines[#lines + 1] = summarize_round(round) end
    write_all(path, table.concat(lines, "\n"))
end

mkdir_p(out_dir)
local manifest = Builder.read_json(manifest_path)
local policy = Builder.default_policy {
    max_depth = max_depth,
    max_arity = max_arity,
    -- Keep the first generator deterministic/small. Raise these as evidence
    -- quality improves.
    max_promotions_per_round = 64,
    max_variants = 2,
    min_hits = 1,
    min_benefit = 1,
}

-- Evidence hook: until trace-motif persistence is wired, use wildcard hits.
-- The generator code already accepts a motif->hits map.
local evidence = { ["*"] = 10 }
local plan = Builder.build_promotion_plan(manifest, policy, evidence)

-- AOT fixture generation: compile all compound candidates to physical bytes
local function try_build_fixtures(promotion_plan)
    local to_compile = {}
    for _, candidate in ipairs(promotion_plan.library or {}) do
        if candidate.kind == "compound_candidate" and
           candidate.replacement and candidate.replacement.kind == "code_stencil_needed" then
            to_compile[#to_compile + 1] = candidate
        end
    end

    if #to_compile == 0 then
        io.stderr:write("No compound candidates to compile\n")
        return promotion_plan
    end

    io.stderr:write(string.format("Building fixtures for %d compound candidates...\n", #to_compile))

    local compiled, err = fixture_builder.compile_and_extract(to_compile)
    if not compiled then
        io.stderr:write(string.format("WARNING: fixture generation failed: %s\n", err))
        io.stderr:write("Continuing with promotion plan (compounds will lack physical data)\n")
        return promotion_plan
    end

    local updated, err = fixture_builder.populate_physical(promotion_plan, compiled)
    if not updated then
        io.stderr:write(string.format("WARNING: physical population failed: %s\n", err))
        return promotion_plan
    end

    return updated
end

plan = try_build_fixtures(plan)

local json_path = out_dir .. "/promotion_plan.json"
local report_path = out_dir .. "/promotion_report.md"
write_all(json_path, Contracts.encode_json(plan) .. "\n")
write_report(report_path, plan)

print("wrote " .. json_path)
print("wrote " .. report_path)
print(string.format("primitives=%d library=%d rounds=%d", plan.primitive_count, plan.library_count, #(plan.rounds or {})))
