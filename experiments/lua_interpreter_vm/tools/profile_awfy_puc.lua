-- Instrument PUC Lua 5.5's interpreter loop and run Are We Fast Yet Lua.
--
-- This is a miner-input tool. It does not touch .vendor/Lua in place. It copies
-- the Lua source tree into build/, patches lvm.c in the copy, builds a profiling
-- lua binary, runs AWFY Lua benchmarks, and writes:
--
--   * opcode histograms
--   * opcode pair/triple histograms
--   * bounded dynamic traces with proto+pc+opcode
--   * repeat-distance spectra (PC-space autocorrelation)
--   * opcode autocorrelation spectra
--   * exact repeated motifs from repeated (proto, pc) anchors
--
-- Usage:
--   luajit experiments/lua_interpreter_vm/tools/profile_awfy_puc.lua [awfy_dir] [quick|full]
--
-- If awfy_dir is omitted, the tool clones are-we-fast-yet into build/.

local AWFY_URL = "https://github.com/smarr/are-we-fast-yet.git"
local DEFAULT_OUT = "experiments/lua_interpreter_vm/build/awfy_puc_profile"
local LUA_SRC = ".vendor/Lua"

local awfy_dir = arg[1]
local mode = arg[2] or os.getenv("MOONLIFT_AWFY_PROFILE_MODE") or "full"
if awfy_dir == "quick" or awfy_dir == "full" then
    mode = awfy_dir
    awfy_dir = nil
end

local function cwd()
    local p = assert(io.popen("pwd"))
    local s = assert(p:read("*l"))
    p:close()
    return s
end

local function abs(path)
    if tostring(path):sub(1, 1) == "/" then return path end
    return cwd() .. "/" .. path
end

local out_root = abs(os.getenv("MOONLIFT_AWFY_PROFILE_OUT") or DEFAULT_OUT)
local lua_build = out_root .. "/puc_lua_profiled"
local profile_root = out_root .. "/profiles"
local awfy_clone = out_root .. "/are-we-fast-yet"

local TRACE_LIMIT = tonumber(os.getenv("MOONLIFT_AWFY_TRACE_LIMIT") or ((mode == "quick") and "200000" or "500000")) or 0
local MAX_PERIOD = tonumber(os.getenv("MOONLIFT_AWFY_MAX_PERIOD") or "256") or 256
local MAX_MOTIF_LEN = tonumber(os.getenv("MOONLIFT_AWFY_MAX_MOTIF_LEN") or "32") or 32

local function q(s)
    return "'" .. tostring(s):gsub("'", "'\\''") .. "'"
end

local function run(cmd)
    io.stderr:write("$ " .. cmd .. "\n")
    local ok = os.execute(cmd)
    if ok ~= true and ok ~= 0 then error("command failed: " .. cmd) end
end

local function read_all(path)
    local f = assert(io.open(path, "rb"))
    local s = f:read("*a")
    f:close()
    return s
end

local function write_all(path, s)
    local f = assert(io.open(path, "wb"))
    f:write(s)
    f:close()
end

local function exists(path)
    local f = io.open(path, "rb")
    if f then f:close(); return true end
    return false
end

local profiler_c = [[

/* Moonlift AWFY dynamic opcode profiler. Generated by profile_awfy_puc.lua. */
#include <stdint.h>
#include "lopnames.h"

struct moonlift_awfy_trace_rec {
  uintptr_t proto;
  int pc;
  unsigned char op;
};

static unsigned long long moonlift_awfy_opcode_counts[NUM_OPCODES];
static unsigned long long moonlift_awfy_pair_counts[NUM_OPCODES][NUM_OPCODES];
static unsigned long long moonlift_awfy_triple_counts[NUM_OPCODES][NUM_OPCODES][NUM_OPCODES];
static int moonlift_awfy_prev1 = -1;
static int moonlift_awfy_prev2 = -1;
static int moonlift_awfy_enabled = -1;
static struct moonlift_awfy_trace_rec *moonlift_awfy_trace = NULL;
static size_t moonlift_awfy_trace_len = 0;
static size_t moonlift_awfy_trace_cap = 0;

static void moonlift_awfy_dump_profile(void) {
  const char *dir = getenv("MOONLIFT_AWFY_PROFILE_DIR");
  char path[4096];
  FILE *f;
  int a, b, c;
  size_t i;
  if (dir == NULL || dir[0] == '\0') return;

  snprintf(path, sizeof(path), "%s/opcodes.tsv", dir);
  f = fopen(path, "w");
  if (f != NULL) {
    fprintf(f, "opcode\tname\tcount\n");
    for (a = 0; a < NUM_OPCODES; a++)
      if (moonlift_awfy_opcode_counts[a] != 0)
        fprintf(f, "%d\t%s\t%llu\n", a, opnames[a], moonlift_awfy_opcode_counts[a]);
    fclose(f);
  }

  snprintf(path, sizeof(path), "%s/pairs.tsv", dir);
  f = fopen(path, "w");
  if (f != NULL) {
    fprintf(f, "op1\tname1\top2\tname2\tcount\n");
    for (a = 0; a < NUM_OPCODES; a++)
      for (b = 0; b < NUM_OPCODES; b++)
        if (moonlift_awfy_pair_counts[a][b] != 0)
          fprintf(f, "%d\t%s\t%d\t%s\t%llu\n", a, opnames[a], b, opnames[b], moonlift_awfy_pair_counts[a][b]);
    fclose(f);
  }

  snprintf(path, sizeof(path), "%s/triples.tsv", dir);
  f = fopen(path, "w");
  if (f != NULL) {
    fprintf(f, "op1\tname1\top2\tname2\top3\tname3\tcount\n");
    for (a = 0; a < NUM_OPCODES; a++)
      for (b = 0; b < NUM_OPCODES; b++)
        for (c = 0; c < NUM_OPCODES; c++)
          if (moonlift_awfy_triple_counts[a][b][c] != 0)
            fprintf(f, "%d\t%s\t%d\t%s\t%d\t%s\t%llu\n", a, opnames[a], b, opnames[b], c, opnames[c], moonlift_awfy_triple_counts[a][b][c]);
    fclose(f);
  }

  if (moonlift_awfy_trace != NULL) {
    snprintf(path, sizeof(path), "%s/trace.tsv", dir);
    f = fopen(path, "w");
    if (f != NULL) {
      fprintf(f, "seq\tproto\tpc\topcode\tname\n");
      for (i = 0; i < moonlift_awfy_trace_len; i++) {
        int op = (int)moonlift_awfy_trace[i].op;
        fprintf(f, "%zu\t%p\t%d\t%d\t%s\n", i,
                (void*)moonlift_awfy_trace[i].proto,
                moonlift_awfy_trace[i].pc,
                op,
                (op >= 0 && op < NUM_OPCODES) ? opnames[op] : "?");
      }
      fclose(f);
    }
  }
}

static void moonlift_awfy_init_if_needed(void) {
  if (moonlift_awfy_enabled < 0) {
    const char *dir = getenv("MOONLIFT_AWFY_PROFILE_DIR");
    moonlift_awfy_enabled = (dir != NULL && dir[0] != '\0');
    if (moonlift_awfy_enabled) {
      const char *limit_s = getenv("MOONLIFT_AWFY_TRACE_LIMIT");
      atexit(moonlift_awfy_dump_profile);
      if (limit_s != NULL && limit_s[0] != '\0') {
        unsigned long long cap = strtoull(limit_s, NULL, 10);
        if (cap > 0) {
          moonlift_awfy_trace_cap = (size_t)cap;
          moonlift_awfy_trace = (struct moonlift_awfy_trace_rec*)malloc(
              moonlift_awfy_trace_cap * sizeof(struct moonlift_awfy_trace_rec));
          if (moonlift_awfy_trace == NULL) moonlift_awfy_trace_cap = 0;
        }
      }
    }
  }
}

static void moonlift_awfy_record_opcode(OpCode op, const Proto *proto, const Instruction *pc_after_fetch) {
  int o = (int)op;
  moonlift_awfy_init_if_needed();
  if (!moonlift_awfy_enabled) return;
  if (o < 0 || o >= NUM_OPCODES) return;
  moonlift_awfy_opcode_counts[o]++;
  if (moonlift_awfy_prev1 >= 0)
    moonlift_awfy_pair_counts[moonlift_awfy_prev1][o]++;
  if (moonlift_awfy_prev2 >= 0)
    moonlift_awfy_triple_counts[moonlift_awfy_prev2][moonlift_awfy_prev1][o]++;
  moonlift_awfy_prev2 = moonlift_awfy_prev1;
  moonlift_awfy_prev1 = o;

  if (moonlift_awfy_trace != NULL && moonlift_awfy_trace_len < moonlift_awfy_trace_cap) {
    moonlift_awfy_trace[moonlift_awfy_trace_len].proto = (uintptr_t)proto;
    moonlift_awfy_trace[moonlift_awfy_trace_len].pc = pcRel(pc_after_fetch, proto);
    moonlift_awfy_trace[moonlift_awfy_trace_len].op = (unsigned char)o;
    moonlift_awfy_trace_len++;
  }
}

#define MOONLIFT_AWFY_RECORD(i_, proto_, pc_) moonlift_awfy_record_opcode(GET_OPCODE(i_), (proto_), (pc_))
]]

local function ensure_awfy()
    if awfy_dir and exists(awfy_dir .. "/benchmarks/Lua/harness.lua") then
        return awfy_dir
    end
    if exists(awfy_clone .. "/benchmarks/Lua/harness.lua") then
        return awfy_clone
    end
    run("mkdir -p " .. q(out_root))
    run("git clone --depth 1 " .. q(AWFY_URL) .. " " .. q(awfy_clone))
    return awfy_clone
end

local function patch_lua_source()
    run("rm -rf " .. q(lua_build))
    run("mkdir -p " .. q(out_root))
    run("cp -R " .. q(LUA_SRC) .. "/. " .. q(lua_build))

    local path = lua_build .. "/lvm.c"
    local s = read_all(path)
    local insert_after = "#include \"lvm.h\"\n"
    assert(s:find(insert_after, 1, true), "lvm.c include anchor not found")
    s = s:gsub(insert_after:gsub("([%^%$%(%)%%%.%[%]%*%+%-%?])", "%%%1"), function()
        return insert_after .. profiler_c
    end, 1)

    local old = [[  i = *(pc++); \
}]]
    local new = [[  i = *(pc++); \
  MOONLIFT_AWFY_RECORD(i, cl->p, pc); \
}]]
    assert(s:find(old, 1, true), "vmfetch anchor not found")
    s = s:gsub(old:gsub("([%^%$%(%)%%%.%[%]%*%+%-%?])", "%%%1"), function()
        return new
    end, 1)
    write_all(path, s)
end

local function build_lua()
    patch_lua_source()
    run("make -C " .. q(lua_build) .. " clean >/dev/null")
    run("make -C " .. q(lua_build) .. " -j2 lua >/dev/null")
end

