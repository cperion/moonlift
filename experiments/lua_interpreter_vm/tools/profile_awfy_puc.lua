local function parse_tsv_counts(path, key_cols, count_col)
    local out = {}
    local f = io.open(path, "rb")
    if not f then return out end
    local first = true
    for line in f:lines() do
        if first then
            first = false
        else
            local cols = {}
            for x in line:gmatch("[^\t]+") do cols[#cols + 1] = x end
            local key = {}
            for _, i in ipairs(key_cols) do key[#key + 1] = cols[i] end
            out[table.concat(key, "\t")] = tonumber(cols[count_col]) or 0
        end
    end
    f:close()
    return out
end

local function add_counts(dst, src)
    for k, v in pairs(src) do dst[k] = (dst[k] or 0) + v end
end

local function sorted_counts(t)
    local arr = {}
    for k, v in pairs(t) do arr[#arr + 1] = { k, v } end
    table.sort(arr, function(a, b) return a[2] > b[2] end)
    return arr
end

local function write_counts(path, header, counts)
    local arr = sorted_counts(counts)
    local f = assert(io.open(path, "wb"))
    f:write(header, "\tcount\n")
    for _, kv in ipairs(arr) do f:write(kv[1], "\t", tostring(kv[2]), "\n") end
    f:close()
end

local function read_trace(path)
    local ops, names, keys = {}, {}, {}
    local f = io.open(path, "rb")
    if not f then return ops, names, keys end
    local first = true
    for line in f:lines() do
        if first then
            first = false
        else
            local seq, proto, pc, op, name = line:match("^([^\t]+)\t([^\t]+)\t([^\t]+)\t([^\t]+)\t([^\t]+)$")
            if seq then
                ops[#ops + 1] = tonumber(op)
                names[#names + 1] = name
                keys[#keys + 1] = proto .. ":" .. pc
            end
        end
    end
    f:close()
    return ops, names, keys
end

local function write_periods(path, periods)
    local arr = sorted_counts(periods)
    local f = assert(io.open(path, "wb"))
    f:write("period\tcount\n")
    for _, kv in ipairs(arr) do f:write(kv[1], "\t", tostring(kv[2]), "\n") end
    f:close()
end

local function compute_autocorr(ops, max_period)
    local out = {}
    local n = #ops
    for p = 1, math.min(max_period, n - 1) do
        local m = 0
        local total = n - p
        for i = p + 1, n do
            if ops[i] == ops[i - p] then m = m + 1 end
        end
        out[p] = { matches = m, total = total }
    end
    return out
end

local function write_autocorr(path, corr)
    local f = assert(io.open(path, "wb"))
    f:write("period\tmatches\ttotal\tscore\n")
    for p = 1, #corr do
        local r = corr[p]
        if r and r.total and r.total > 0 then
            f:write(p, "\t", r.matches, "\t", r.total, "\t", string.format("%.9f", r.matches / r.total), "\n")
        end
    end
    f:close()
end

local function add_corr(dst, src)
    for p, r in pairs(src) do
        local d = dst[p]
        if not d then
            dst[p] = { matches = r.matches, total = r.total }
        else
            d.matches = d.matches + r.matches
            d.total = d.total + r.total
        end
    end
end

local function canonical_rotation(parts)
    local n = #parts
    if n <= 1 then return table.concat(parts, " ") end
    local best = nil
    for start = 1, n do
        local rotated = {}
        for k = 0, n - 1 do rotated[#rotated + 1] = parts[((start + k - 2) % n) + 1] end
        local s = table.concat(rotated, " ")
        if best == nil or s < best then best = s end
    end
    return best
end

local function analyze_trace(dir)
    local ops, names, keys = read_trace(dir .. "/trace.tsv")
    local periods, motifs = {}, {}
    local last = {}
    for i, key in ipairs(keys) do
        local prev = last[key]
        if prev then
            local d = i - prev
            if d <= MAX_PERIOD then
                periods[tostring(d)] = (periods[tostring(d)] or 0) + 1
            end
            if d <= MAX_MOTIF_LEN and d > 1 then
                local parts = {}
                for j = prev, i - 1 do parts[#parts + 1] = names[j] end
                local motif = canonical_rotation(parts)
                motifs[motif] = (motifs[motif] or 0) + 1
            end
        end
        last[key] = i
    end
    local corr = compute_autocorr(ops, MAX_PERIOD)
    write_periods(dir .. "/period_spectrum.tsv", periods)
    write_autocorr(dir .. "/opcode_autocorr.tsv", corr)
    write_counts(dir .. "/motifs.tsv", "motif", motifs)
    return periods, motifs, corr
end

local function aggregate(benches)
    local opcodes, pairs, triples = {}, {}, {}
    local periods, motifs, corr = {}, {}, {}
    for _, b in ipairs(benches) do
        local dir = profile_root .. "/" .. b[1]
        add_counts(opcodes, parse_tsv_counts(dir .. "/opcodes.tsv", { 1, 2 }, 3))
        add_counts(pairs, parse_tsv_counts(dir .. "/pairs.tsv", { 1, 2, 3, 4 }, 5))
        add_counts(triples, parse_tsv_counts(dir .. "/triples.tsv", { 1, 2, 3, 4, 5, 6 }, 7))
        local ps, ms, ac = analyze_trace(dir)
        add_counts(periods, ps)
        add_counts(motifs, ms)
        add_corr(corr, ac)
    end
    write_counts(out_root .. "/aggregate_opcode_hist.tsv", "opcode\tname", opcodes)
    write_counts(out_root .. "/aggregate_pairs.tsv", "op1\tname1\top2\tname2", pairs)
    write_counts(out_root .. "/aggregate_triples.tsv", "op1\tname1\top2\tname2\top3\tname3", triples)
    write_periods(out_root .. "/aggregate_period_spectrum.tsv", periods)
    write_autocorr(out_root .. "/aggregate_opcode_autocorr.tsv", corr)
    write_counts(out_root .. "/aggregate_motifs.tsv", "motif", motifs)

    local report = {}
    local function top(title, counts, n)
        report[#report + 1] = "## " .. title
        report[#report + 1] = ""
        local arr = sorted_counts(counts)
        for i = 1, math.min(n, #arr) do
            report[#report + 1] = string.format("%12d  %s", arr[i][2], arr[i][1]:gsub("\t", " "))
        end
        report[#report + 1] = ""
    end
    report[#report + 1] = "# AWFY PUC Lua Dynamic Opcode Profile"
    report[#report + 1] = ""
    report[#report + 1] = "Mode: `" .. mode .. "`"
    report[#report + 1] = ""
    report[#report + 1] = "Trace limit per benchmark: `" .. tostring(TRACE_LIMIT) .. "`"
    report[#report + 1] = ""
    top("Top opcodes", opcodes, 40)
    top("Top opcode pairs", pairs, 50)
    top("Top opcode triples", triples, 50)
    top("Top repeat periods from repeated (proto, pc) anchors", periods, 40)
    top("Top canonical loop motifs from repeated (proto, pc) anchors", motifs, 40)
    write_all(out_root .. "/PROFILE_REPORT.md", table.concat(report, "\n"))
end

local function main()
    local awfy = ensure_awfy()
    build_lua()
    run("rm -rf " .. q(profile_root))
    run("mkdir -p " .. q(profile_root))

    local lua_bin = lua_build .. "/lua"
    local lua_bench_dir = awfy .. "/benchmarks/Lua"
    for _, b in ipairs(canonical) do
        local name = b[1]
        local inner = (mode == "quick") and b[3] or b[2]
        local dir = profile_root .. "/" .. name
        run("mkdir -p " .. q(dir))
        local cmd = string.format(
            "cd %s && MOONLIFT_AWFY_PROFILE_DIR=%s MOONLIFT_AWFY_TRACE_LIMIT=%d %s harness.lua %s 1 %d > %s 2>&1",
            q(lua_bench_dir), q(dir), TRACE_LIMIT, q(lua_bin), q(name), inner, q(dir .. "/run.log"))
        run(cmd)
    end
    aggregate(canonical)
    print("wrote " .. out_root .. "/PROFILE_REPORT.md")
    print("wrote " .. out_root .. "/aggregate_opcode_hist.tsv")
    print("wrote " .. out_root .. "/aggregate_pairs.tsv")
    print("wrote " .. out_root .. "/aggregate_triples.tsv")
    print("wrote " .. out_root .. "/aggregate_period_spectrum.tsv")
    print("wrote " .. out_root .. "/aggregate_opcode_autocorr.tsv")
    print("wrote " .. out_root .. "/aggregate_motifs.tsv")
end

main()
