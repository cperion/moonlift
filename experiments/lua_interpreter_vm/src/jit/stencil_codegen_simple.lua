-- Simplified StateOp → Moonlift code generation.
-- Just generate stub functions that compile; real semantics matter less since
-- these will be replaced with machine code at runtime.

local M = {}

-- Generate a simple stub function from a StateOp sequence
-- Just emit code that returns a pointer, matching our ABI
function M.generate_function(candidate)
    local name = candidate.name or ("compound_" .. candidate.id)
    local sanitized_name = string.gsub(name, "[^a-z0-9_]", "_")
    local ops = candidate.ops or {}

    if not ops or #ops == 0 then
        return nil, "no operations in candidate"
    end

    local lines = {}
    local holes_list = {}

    lines[#lines + 1] = string.format("func %s(state: ptr(u8), exits: ptr(u8), return_addr: ptr(u8)) -> ptr(u8)", sanitized_name)
    lines[#lines + 1] = "    return return_addr"
    lines[#lines + 1] = "end"
    lines[#lines + 1] = ""

    local source = table.concat(lines, "\n")

    -- For stub generation, we don't extract holes from the real code
    -- In the full version, holes come from scanning the compiled binary

    return {
        source = source,
        holes = holes_list,
        name = sanitized_name,
        original_name = name
    }
end

-- Generate a module with all stencil functions
function M.generate_module(candidates)
    if not candidates or #candidates == 0 then
        return nil, "no candidates"
    end

    local lines = {}
    lines[#lines + 1] = "-- Generated stencil functions (stub version)"
    lines[#lines + 1] = "-- Auto-generated by stencil_codegen_simple.lua"
    lines[#lines + 1] = ""

    local generated = {}
    for i, candidate in ipairs(candidates) do
        if candidate.replacement and candidate.replacement.kind == "code_stencil_needed" then
            local func, err = M.generate_function(candidate)
            if func then
                for line in string.gmatch(func.source, "[^\n]+") do
                    lines[#lines + 1] = line
                end
                generated[#generated + 1] = {
                    name = func.name,  -- sanitized name for ELF lookup
                    original_name = candidate.name,  -- original name for promotion plan mapping
                    id = candidate.id,
                    holes = func.holes
                }
            else
                io.stderr:write(string.format("warning: skipped %s: %s\n", candidate.name, err))
            end
        end
    end

    return {
        source = table.concat(lines, "\n"),
        generated = generated
    }
end

return M
