local T = pvm.context(); Schema.Define(T)
local lowered = Pipeline.Define(T).parse_and_lower_c(src, { site = "emit_c_api_lower" })
assert(lowered.code_module ~= nil, "C pipeline should expose MoonCode module")
assert(lowered.code_report ~= nil and #lowered.code_report.issues == 0, "expected no Code validation issues from API source")
assert(#lowered.c_report.issues == 0, "expected no validation issues from API source")
assert(lowered.c_unit.target.pointer_bits == 64 and lowered.c_unit.target.index_bits == 64, "expected native64 default target")

local Host = require("moonlift.mlua_run")
local bundle = Host.eval [[
local add = func hosted_add(a: i32, b: i32): i32
    return a + b
end
local M = moon.module("emit_c_host_bundle")
M:add_func(add)
return M
]]

local bundle_path = os.tmpname() .. ".c"
local bundle_artifact_path = bundle:c_artifact(bundle_path)
local bundle_src = bundle_artifact_path.source
assert(bundle_src:match("int32_t hosted_add%("), "expected hosted_add in bundle C source")
local bf = assert(io.open(bundle_path, "rb"))
local bundle_written = bf:read("*a")
bf:close()
os.remove(bundle_path)
assert(bundle_written == bundle_src, "BundleValue:c_artifact should write the returned source")
syntax_check(bundle_src, "BundleValue:c_artifact source output")
local bundle_emit_src = bundle:emit_c_artifact().source
assert(bundle_emit_src:match("int32_t hosted_add%("), "BundleValue:emit_c_artifact should expose C source")
local bundle_artifact = bundle:emit_c_artifact({ support_source = "/* bundle support */\n" })
assert(bundle_artifact.header:match("hosted_add"), "BundleValue:emit_c_artifact should generate a header")
assert(bundle_artifact.combined:match("bundle support"), "BundleValue:emit_c_artifact should combine support")
syntax_check(bundle_artifact.header, "BundleValue:emit_c_artifact header")
syntax_check(bundle_artifact.combined, "BundleValue:emit_c_artifact combined")

local M2 = moon.module("emit_c_export_bundle")
M2:export_func("hosted_add_export", { moon.param("a", moon.i32), moon.param("b", moon.i32) }, moon.i32, function(fn)
    fn:return_(moon.expr[[a + b]])
end)
local export_src = M2:emit_c_artifact().source
assert(export_src:match("int32_t hosted_add_export%("), "exported bundle C source should contain hosted_add_export")
if exec_ok("command -v cc >/dev/null 2>&1") then
    local compiled = M2:compile_c({ keep = false })
    local add_export = compiled:get("hosted_add_export")
    assert(add_export(20, 22) == 42, "BundleValue:compile_c exported function should execute")
    compiled:free()
else
    io.write("cc not found; skipping BundleValue:compile_c execution check\n")
end

io.write("moonlift emit_c_api ok\n")
